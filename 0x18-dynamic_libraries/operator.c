#include <stdio.h>

/*
 * add function
 */

int add(int a, int b)
{
	return (a + b);
}


/*
 * division function
 */

int div(int a, int b)
{
	return (a / b);
}


/*
 * multiplication function
 */

int mul(int a, int b)
{
	return (a * b);
}


/*
 * mod function
 */

int mod(int a, int b)
{
	return (a % b);
}


/*
 * subtract
 */

int sub(int a, int b)
{
	return (a - b);
}
